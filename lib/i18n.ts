const COOKIE_NAME = 'asklizy_lang';
const STORAGE_KEY = 'asklizy_lang';

export function getLang(): Lang {
  if (typeof window === 'undefined') {
    return 'fr';
  }
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'fr' || stored === 'en') return stored;
  } catch {}
  return 'fr';
}

export function setLang(lang: Lang) {
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch {}
  try {
    document.cookie = `${COOKIE_NAME}=${lang};path=/;max-age=${60 * 60 * 24 * 365};samesite=lax`;
  } catch {}
}

export function t(lang: Lang, key: string): string {
  return (translations[lang] as Record<string, string>)?.[key] || (translations.en as Record<string, string>)[key] || key;
}
